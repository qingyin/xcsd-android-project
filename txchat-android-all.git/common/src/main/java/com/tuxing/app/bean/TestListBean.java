package com.tuxing.app.bean;

public class TestListBean {

	private int id;

	private String name;

	private String description;

	private String associateTag;

	private int color;

	private String colorValue;

	private int status;
	
	private String animalPic;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getAssociateTag() {
		return associateTag;
	}

	public void setAssociateTag(String associateTag) {
		this.associateTag = associateTag;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public String getColorValue() {
		return colorValue;
	}

	public void setColorValue(String colorValue) {
		this.colorValue = colorValue;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getAnimalPic() {
		return animalPic;
	}

	public void setAnimalPic(String animalPic) {
		this.animalPic = animalPic;
	}
	
}
